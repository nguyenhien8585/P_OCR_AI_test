class SimpleImageExtractor:
    """Class ƒë∆°n gi·∫£n v√† ·ªïn ƒë·ªãnh ƒë·ªÉ t√°ch ·∫£nh/b·∫£ng"""
    
    def __init__(self):
        self.min_area_ratio = 0.005
        self.min_area_abs = 2000
        self.min_width = 60
        self.min_height = 60
        self.max_figures = 15
        self.padding = 20
        self.confidence_threshold = 40
    
    def extract_figures_and_tables(self, image_bytes):
        """T√°ch ·∫£nh v√† b·∫£ng v·ªõi thu·∫≠t to√°n ƒë∆°n gi·∫£n, ·ªïn ƒë·ªãnh"""
        if not CV2_AVAILABLE:
            return [], 0, 0
        
        # ƒê·ªçc ·∫£nh
        img_pil = Image.open(io.BytesIO(image_bytes)).convert("RGB")
        img = np.array(img_pil)
        h, w = img.shape[:2]
        
        # Ti·ªÅn x·ª≠ l√Ω ƒë∆°n gi·∫£n
        gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)
        
        # L√†m m·ªãn
        gray = cv2.GaussianBlur(gray, (5, 5), 0)
        
        # Ph√°t hi·ªán c·∫°nh
        edges = cv2.Canny(gray, 50, 150)
        
        # Dilate ƒë·ªÉ n·ªëi c√°c th√†nh ph·∫ßn
        kernel = np.ones((3, 3), np.uint8)
        edges = cv2.dilate(edges, kernel, iterations=2)
        
        # Morphological closing ƒë·ªÉ l·∫•p kho·∫£ng tr·ªëng
        kernel_close = cv2.getStructuringElement(cv2.MORPH_RECT, (10, 10))
        closed = cv2.morphologyEx(edges, cv2.MORPH_CLOSE, kernel_close)
        
        # T√¨m contours
        contours, _ = cv2.findContours(closed, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        
        candidates = []
        for cnt in contours:
            x, y, ww, hh = cv2.boundingRect(cnt)
            area = ww * hh
            area_ratio = area / (w * h)
            aspect_ratio = ww / (hh + 1e-6)
            
            # L·ªçc c∆° b·∫£n
            if area < self.min_area_abs or area_ratio < self.min_area_ratio or area_ratio > 0.8:
                continue
            
            if ww < self.min_width or hh < self.min_height:
                continue
            
            if not (0.2 < aspect_ratio < 10.0):
                continue
            
            # Lo·∫°i b·ªè v√πng ·ªü r√¨a
            margin = 0.03
            if (x < margin*w or y < margin*h or 
                (x+ww) > (1-margin)*w or (y+hh) > (1-margin)*h):
                continue
            
            # T√≠nh ƒë·∫∑c tr∆∞ng ƒë∆°n gi·∫£n
            hull = cv2.convexHull(cnt)
            hull_area = cv2.contourArea(hull)
            contour_area = cv2.contourArea(cnt)
            
            if hull_area == 0 or contour_area < 100:
                continue
            
            solidity = float(contour_area) / hull_area
            extent = float(contour_area) / area
            
            if solidity < 0.3 or extent < 0.2:
                continue
            
            # Ph√¢n lo·∫°i ƒë∆°n gi·∫£n
            is_table = self._is_table_simple(ww, hh, aspect_ratio, w, h)
            
            # T√≠nh confidence ƒë∆°n gi·∫£n
            confidence = self._calculate_confidence_simple(area_ratio, aspect_ratio, solidity, extent)
            
            if confidence >= self.confidence_threshold:
                candidates.append({
                    "area": area,
                    "x0": x, "y0": y, "x1": x+ww, "y1": y+hh,
                    "is_table": is_table,
                    "confidence": confidence,
                    "aspect_ratio": aspect_ratio,
                    "solidity": solidity,
                    "extent": extent,
                    "bbox": (x, y, ww, hh),
                    "center_y": y + hh // 2,
                    "y_position": y
                })
        
        # S·∫Øp x·∫øp v√† l·ªçc overlap
        candidates = sorted(candidates, key=lambda f: f['confidence'], reverse=True)
        candidates = self._filter_overlapping_simple(candidates)
        candidates = candidates[:self.max_figures]
        candidates = sorted(candidates, key=lambda box: (box["y0"], box["x0"]))
        
        # T·∫°o ·∫£nh k·∫øt qu·∫£
        final_figures = []
        img_idx = 0
        table_idx = 0
        
        for fig_data in candidates:
            # Padding ƒë∆°n gi·∫£n
            x0 = max(0, fig_data["x0"] - self.padding)
            y0 = max(0, fig_data["y0"] - self.padding)
            x1 = min(w, fig_data["x1"] + self.padding)
            y1 = min(h, fig_data["y1"] + self.padding)
            
            crop = img[y0:y1, x0:x1]
            
            if crop.size == 0:
                continue
            
            # Chuy·ªÉn th√†nh base64
            buf = io.BytesIO()
            Image.fromarray(crop).save(buf, format="JPEG", quality=95)
            b64 = base64.b64encode(buf.getvalue()).decode()
            
            # ƒê·∫∑t t√™n file
            if fig_data["is_table"]:
                name = f"table-{table_idx+1}.jpeg"
                table_idx += 1
            else:
                name = f"img-{img_idx+1}.jpeg"
                img_idx += 1
            
            final_figures.append({
                "name": name,
                "base64": b64,
                "is_table": fig_data["is_table"],
                "bbox": (x0, y0, x1-x0, y1-y0),
                "original_bbox": fig_data["bbox"],
                "confidence": fig_data["confidence"],
                "aspect_ratio": fig_data["aspect_ratio"],
                "area": fig_data["area"],
                "solidity": fig_data["solidity"],
                "extent": fig_data["extent"],
                "center_y": fig_data["center_y"],
                "y_position": fig_data["y_position"]
            })
        
        return final_figures, h, w
    
    def _is_table_simple(self, w, h, aspect_ratio, img_w, img_h):
        """Ph√¢n lo·∫°i table/image ƒë∆°n gi·∫£n"""
        # Table th∆∞·ªùng c√≥ aspect ratio ngang v√† chi·∫øm nhi·ªÅu chi·ªÅu r·ªông
        is_wide = aspect_ratio > 1.5
        is_substantial_width = w > 0.3 * img_w
        is_reasonable_height = 0.1 * img_h < h < 0.8 * img_h
        
        return is_wide and is_substantial_width and is_reasonable_height
    
    def _calculate_confidence_simple(self, area_ratio, aspect_ratio, solidity, extent):
        """T√≠nh confidence ƒë∆°n gi·∫£n"""
        confidence = 0
        
        # ƒêi·ªÉm t·ª´ area
        if 0.02 < area_ratio < 0.6:
            confidence += 50
        elif 0.01 < area_ratio < 0.8:
            confidence += 30
        else:
            confidence += 10
        
        # ƒêi·ªÉm t·ª´ aspect ratio
        if 0.5 < aspect_ratio < 3.0:
            confidence += 25
        elif 0.3 < aspect_ratio < 5.0:
            confidence += 15
        else:
            confidence += 5
        
        # ƒêi·ªÉm t·ª´ shape quality
        if solidity > 0.7:
            confidence += 15
        elif solidity > 0.5:
            confidence += 10
        else:
            confidence += 5
        
        if extent > 0.5:
            confidence += 10
        elif extent > 0.3:
            confidence += 5
        
        return min(100, confidence)
    
    def _filter_overlapping_simple(self, candidates):
        """L·ªçc overlap ƒë∆°n gi·∫£n"""
        filtered = []
        
        for candidate in candidates:
            is_overlap = False
            x0, y0, x1, y1 = candidate['x0'], candidate['y0'], candidate['x1'], candidate['y1']
            area1 = (x1-x0) * (y1-y0)
            
            for other in filtered:
                ox0, oy0, ox1, oy1 = other['x0'], other['y0'], other['x1'], other['y1']
                area2 = (ox1-ox0) * (oy1-oy0)
                
                # T√≠nh IoU
                intersection_area = max(0, min(x1, ox1) - max(x0, ox0)) * max(0, min(y1, oy1) - max(y0, oy0))
                union_area = area1 + area2 - intersection_area
                
                if union_area > 0:
                    iou = intersection_area / union_area
                    if iou > 0.3:
                        is_overlap = True
                        break
            
            if not is_overlap:
                filtered.append(candidate)
        
        return filtered
    
    def insert_figures_into_text_smart(self, text, figures, img_h, img_w):
        """Ch√®n ·∫£nh v√†o vƒÉn b·∫£n th√¥ng minh - k·∫øt h·ª£p t·ª´ kh√≥a + v·ªã tr√≠"""
        if not figures:
            return text
        
        lines = text.split('\n')
        result_lines = lines[:]
        
        # S·∫Øp x·∫øp figures theo v·ªã tr√≠ Y
        sorted_figures = sorted(figures, key=lambda f: f['y_position'])
        
        # Ph√¢n t√≠ch c·∫•u tr√∫c c√¢u h·ªèi
        question_blocks = self._identify_question_blocks(lines)
        
        # Ch√®n figures v√†o t·ª´ng question block
        inserted_count = 0
        used_figures = set()
        
        for fig in sorted_figures:
            if fig['name'] in used_figures:
                continue
            
            # T√¨m question block ph√π h·ª£p
            best_position = self._find_best_insertion_position(fig, question_blocks, lines, img_h)
            
            if best_position is not None:
                insertion_index = best_position + inserted_count
                
                if insertion_index <= len(result_lines):
                    tag = f"\n[B·∫¢NG: {fig['name']}]\n" if fig['is_table'] else f"\n[H√åNH: {fig['name']}]\n"
                    result_lines.insert(insertion_index, tag)
                    inserted_count += 1
                    used_figures.add(fig['name'])
        
        return '\n'.join(result_lines)
    
    def _identify_question_blocks(self, lines):
        """Nh·∫≠n di·ªán c√°c kh·ªëi c√¢u h·ªèi"""
        blocks = []
        current_block = None
        
        for i, line in enumerate(lines):
            line_content = line.strip()
            
            # B·∫Øt ƒë·∫ßu c√¢u h·ªèi m·ªõi
            if re.match(r'^c√¢u\s+\d+', line_content.lower()):
                if current_block:
                    blocks.append(current_block)
                
                current_block = {
                    'question_line': i,
                    'question_number': self._extract_question_number(line_content),
                    'description_lines': [],
                    'answer_start': None,
                    'answer_lines': []
                }
            
            elif current_block:
                # T√¨m ƒëi·ªÉm ch√®n t·ªëi ∆∞u
                if any(keyword in line_content.lower() for keyword in [
                    'x√©t t√≠nh ƒë√∫ng sai', 'kh·∫≥ng ƒë·ªãnh sau:', 'sau:', 'cho h√¨nh', 'trong h√¨nh'
                ]):
                    current_block['description_lines'].append(i)
                
                # Nh·∫≠n di·ªán ƒë√°p √°n
                elif re.match(r'^[a-d]\)', line_content) or re.match(r'^[A-D]\)', line_content):
                    if current_block['answer_start'] is None:
                        current_block['answer_start'] = i
                    current_block['answer_lines'].append(i)
        
        if current_block:
            blocks.append(current_block)
        
        return blocks
    
    def _extract_question_number(self, line_content):
        """Tr√≠ch xu·∫•t s·ªë c√¢u h·ªèi"""
        match = re.search(r'c√¢u\s+(\d+)', line_content.lower())
        return int(match.group(1)) if match else None
    
    def _find_best_insertion_position(self, figure, question_blocks, lines, img_h):
        """T√¨m v·ªã tr√≠ ch√®n t·ªët nh·∫•t"""
        fig_y = figure['y_position']
        
        best_score = 0
        best_position = None
        
        for block in question_blocks:
            # T√≠nh ƒëi·ªÉm cho c√°c v·ªã tr√≠ trong block
            
            # V·ªã tr√≠ 1: Sau description lines (tr∆∞·ªõc ƒë√°p √°n)
            if block['description_lines'] and block['answer_start']:
                for desc_line in block['description_lines']:
                    line_content = lines[desc_line].strip().lower()
                    
                    # ∆Øu ti√™n v·ªã tr√≠ sau d√≤ng c√≥ t·ª´ kh√≥a ƒë·∫∑c bi·ªát
                    position_score = 50
                    
                    if 'kh·∫≥ng ƒë·ªãnh sau:' in line_content or line_content.endswith('sau:'):
                        position_score += 40
                    elif 'x√©t t√≠nh ƒë√∫ng sai' in line_content:
                        position_score += 30
                    elif 'cho h√¨nh' in line_content or 'trong h√¨nh' in line_content:
                        position_score += 25
                    
                    # ƒêi·ªÉm t·ª´ v·ªã tr√≠ Y
                    estimated_line_y = (desc_line / len(lines)) * img_h
                    y_distance = abs(estimated_line_y - fig_y) / img_h
                    y_score = max(0, 30 - y_distance * 30)
                    
                    total_score = position_score + y_score
                    
                    if total_score > best_score:
                        best_score = total_score
                        best_position = desc_line + 1
            
            # V·ªã tr√≠ 2: Sau question line (n·∫øu kh√¥ng c√≥ description t·ªët)
            elif block['question_line'] and best_score < 40:
                estimated_line_y = (block['question_line'] / len(lines)) * img_h
                y_distance = abs(estimated_line_y - fig_y) / img_h
                total_score = 30 - y_distance * 20
                
                if total_score > best_score:
                    best_score = total_score
                    best_position = block['question_line'] + 1
        
        return best_position if best_score > 20 else None
    
    def create_debug_image(self, image_bytes, figures):
        """T·∫°o ·∫£nh debug ƒë∆°n gi·∫£n"""
        img_pil = Image.open(io.BytesIO(image_bytes)).convert("RGB")
        draw = ImageDraw.Draw(img_pil)
        
        colors = ['red', 'blue', 'green', 'orange', 'purple', 'brown', 'pink', 'cyan']
        
        for i, fig in enumerate(figures):
            color = colors[i % len(colors)]
            bbox = fig['original_bbox']
            x, y, w, h = bbox
            
            # V·∫Ω khung
            thickness = 3
            draw.rectangle([x, y, x+w, y+h], outline=color, width=thickness)
            
            # V·∫Ω label
            type_label = "TBL" if fig['is_table'] else "IMG"
            label = f"{fig['name']}\n{type_label}: {fig['confidence']:.0f}%\nY: {fig['y_position']}"
            
            # V·∫Ω text background
            lines = label.split('\n')
            max_width = max(len(line) for line in lines) * 8
            text_height = len(lines) * 15
            draw.rectangle([x, y-text_height-5, x+max_width, y], fill=color, outline=color)
            
            # V·∫Ω text
            for j, line in enumerate(lines):
                draw.text((x+2, y-text_height+j*13), line, fill='white')
        
        return img_pil

class GeminiAPI:
    def __init__(self, api_key: str):
        self.api_key = api_key
        self.base_url = "https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent"
    
    def encode_image(self, image_data: bytes) -> str:
        return base64.b64encode(image_data).decode('utf-8')
    
    def convert_to_latex(self, content_data: bytes, content_type: str, prompt: str) -> str:
        headers = {"Content-Type": "application/json"}
        
        if content_type.startswith('image/'):
            mime_type = content_type
        else:
            mime_type = "image/png"
        
        encoded_content = self.encode_image(content_data)
        
        payload = {
            "contents": [
                {
                    "parts": [
                        {"text": prompt},
                        {
                            "inline_data": {
                                "mime_type": mime_type,
                                "data": encoded_content
                            }
                        }
                    ]
                }
            ],
            "generationConfig": {
                "temperature": 0.1,
                "topK": 1,
                "topP": 0.8,
                "maxOutputTokens": 8192,
            }
        }
        
        try:
            response = requests.post(
                f"{self.base_url}?key={self.api_key}",
                headers=headers,
                json=payload,
                timeout=90
            )
            
            if response.status_code == 200:
                result = response.json()
                if 'candidates' in result and len(result['candidates']) > 0:
                    content = result['candidates'][0]['content']['parts'][0]['text']
                    return content.strip()
                else:
                    raise Exception("API kh√¥ng tr·∫£ v·ªÅ k·∫øt qu·∫£ h·ª£p l·ªá")
            elif response.status_code == 401:
                raise Exception("API key kh√¥ng h·ª£p l·ªá ho·∫∑c ƒë√£ h·∫øt h·∫°n")
            elif response.status_code == 429:
                raise Exception("ƒê√£ v∆∞·ª£t qu√° gi·ªõi h·∫°n rate limit")
            else:
                raise Exception(f"API Error {response.status_code}: {response.text}")
        
        except requests.exceptions.Timeout:
            raise Exception("Request timeout - th·ª≠ l·∫°i sau √≠t ph√∫t")
        except requests.exceptions.ConnectionError:
            raise Exception("L·ªói k·∫øt n·ªëi m·∫°ng")
        except Exception as e:
            raise Exception(str(e))

class PDFProcessor:
    @staticmethod
    def extract_images_and_text(pdf_file):
        pdf_document = fitz.open(stream=pdf_file.read(), filetype="pdf")
        images = []
        
        for page_num in range(pdf_document.page_count):
            page = pdf_document[page_num]
            mat = fitz.Matrix(2.5, 2.5)
            pix = page.get_pixmap(matrix=mat)
            img_data = pix.tobytes("png")
            img = Image.open(io.BytesIO(img_data))
            images.append((img, page_num + 1))
        
        pdf_document.close()
        return images

class SimpleWordExporter:
    @staticmethod
    def create_word_document(latex_content: str, extracted_figures=None, images=None) -> io.BytesIO:
        doc = Document()
        
        # Th√™m ti√™u ƒë·ªÅ
        title = doc.add_heading('T√†i li·ªáu ƒë√£ chuy·ªÉn ƒë·ªïi t·ª´ PDF/·∫¢nh', 0)
        title.alignment = 1
        
        doc.add_paragraph(f"ƒê∆∞·ª£c t·∫°o b·ªüi PDF/Image to LaTeX Converter")
        doc.add_paragraph(f"Th·ªùi gian: {time.strftime('%Y-%m-%d %H:%M:%S')}")
        doc.add_paragraph("")
        
        # X·ª≠ l√Ω n·ªôi dung ƒë∆°n gi·∫£n
        lines = latex_content.split('\n')
        
        for line in lines:
            line = line.strip()
            
            # B·ªè qua code blocks
            if line.startswith('```') or line.endswith('```'):
                continue
            
            # X·ª≠ l√Ω tag ·∫£nh/b·∫£ng
            if line.startswith('[H√åNH:') and line.endswith(']'):
                img_name = line.replace('[H√åNH:', '').replace(']', '').strip()
                SimpleWordExporter._insert_extracted_image(doc, img_name, extracted_figures, "H√¨nh minh h·ªça")
                continue
            elif line.startswith('[B·∫¢NG:') and line.endswith(']'):
                img_name = line.replace('[B·∫¢NG:', '').replace(']', '').strip()
                SimpleWordExporter._insert_extracted_image(doc, img_name, extracted_figures, "B·∫£ng s·ªë li·ªáu")
                continue
            
            # Skip comments
            if line.startswith('<!--') and line.endswith('-->'):
                if 'Trang' in line or '·∫¢nh' in line:
                    doc.add_heading(line.replace('<!--', '').replace('-->', '').strip(), level=2)
                continue
            
            if not line:
                continue
            
            # X·ª≠ l√Ω c√¥ng th·ª©c ƒë∆°n gi·∫£n - chuy·ªÉn v·ªÅ text
            if '${' in line and '}$' in line:
                # X·ª≠ l√Ω equation ƒë∆°n gi·∫£n
                processed_line = SimpleWordExporter._process_simple_equations(line)
                p = doc.add_paragraph(processed_line)
                run = p.runs[0] if p.runs else p.add_run("")
                run.font.name = 'Times New Roman'
                run.font.size = Pt(12)
            else:
                # ƒêo·∫°n vƒÉn b√¨nh th∆∞·ªùng
                p = doc.add_paragraph(line)
                run = p.runs[0] if p.runs else p.add_run("")
                run.font.name = 'Times New Roman'
                run.font.size = Pt(12)
        
        # Th√™m ·∫£nh g·ªëc n·∫øu c√≥
        if images and not extracted_figures:
            doc.add_page_break()
            doc.add_heading('H√¨nh ·∫£nh g·ªëc', level=1)
            
            for i, img in enumerate(images):
                try:
                    doc.add_heading(f'H√¨nh {i+1}', level=2)
                    
                    if img.mode in ('RGBA', 'LA', 'P'):
                        img = img.convert('RGB')
                    
                    with tempfile.NamedTemporaryFile(suffix='.png', delete=False) as tmp:
                        img.save(tmp.name, 'PNG')
                        try:
                            doc.add_picture(tmp.name, width=doc.sections[0].page_width * 0.8)
                        except Exception:
                            doc.add_paragraph(f"[H√¨nh ·∫£nh {i+1} - Kh√¥ng th·ªÉ hi·ªÉn th·ªã]")
                        os.unlink(tmp.name)
                except Exception:
                    doc.add_paragraph(f"[L·ªói hi·ªÉn th·ªã h√¨nh {i+1}]")
        
        buffer = io.BytesIO()
        doc.save(buffer)
        buffer.seek(0)
        return buffer
    
    @staticmethod
    def _process_simple_equations(line):
        """X·ª≠ l√Ω equations ƒë∆°n gi·∫£n - chuy·ªÉn v·ªÅ text"""
        result = line
        
        # LaTeX to Unicode mapping ƒë∆°n gi·∫£n
        replacements = {
            '\\alpha': 'Œ±', '\\beta': 'Œ≤', '\\gamma': 'Œ≥', '\\delta': 'Œ¥',
            '\\theta': 'Œ∏', '\\lambda': 'Œª', '\\mu': 'Œº', '\\pi': 'œÄ',
            '\\sigma': 'œÉ', '\\phi': 'œÜ', '\\omega': 'œâ',
            '\\leq': '‚â§', '\\geq': '‚â•', '\\neq': '‚â†', '\\approx': '‚âà',
            '\\times': '√ó', '\\div': '√∑', '\\pm': '¬±', '\\infty': '‚àû',
            '\\perp': '‚ä•', '\\parallel': '‚à•', '\\angle': '‚à†', '\\degree': '¬∞'
        }
        
        # X·ª≠ l√Ω c√°c c√¥ng th·ª©c ƒë∆°n gi·∫£n
        while '${' in result and '}$' in result:
            start = result.find('${')
            end = result.find('}$', start)
            
            if start != -1 and end != -1:
                equation = result[start+2:end]
                
                # Thay th·∫ø symbols
                for latex, unicode_char in replacements.items():
                    equation = equation.replace(latex, unicode_char)
                
                # X·ª≠ l√Ω fractions ƒë∆°n gi·∫£n
                equation = re.sub(r'\\frac\{([^}]+)\}\{([^}]+)\}', r'(\1)/(\2)', equation)
                
                # X·ª≠ l√Ω superscript/subscript ƒë∆°n gi·∫£n
                equation = re.sub(r'\^\{([^}]+)\}', r'^(\1)', equation)
                equation = re.sub(r'_\{([^}]+)\}', r'_(\1)', equation)
                
                # Lo·∫°i b·ªè c√°c command kh√°c
                equation = re.sub(r'\\[a-zA-Z]+', '', equation)
                equation = equation.replace('{', '').replace('}', '')
                
                result = result[:start] + equation + result[end+2:]
            else:
                break
        
        return result
    
    @staticmethod
    def _insert_extracted_image(doc, img_name, extracted_figures, caption_prefix):
        if not extracted_figures:
            doc.add_paragraph(f"[{caption_prefix}: {img_name} - Kh√¥ng t√¨m th·∫•y]")
